def solver(eq):
    eq = eq.replace(" ", "")
    eq = eq.split("\n")

    # get variables
    variables = []
    for line in eq:
        if "x_" in line:
            variables.append(line.split("=")[0])
    variables = sorted(variables)

    # get coefficients
    coefficients = []
    for line in eq:
        if "x_" in line:
            coefficients.append(line.split("=")[1])
    coefficients = sorted(coefficients)

    # get constants
    constants = []
    for line in eq:
        if "x_" not in line:
            constants.append(line.split("=")[1])
    constants = sorted(constants)

    # get coefficients of variables
    coefficients_of_variables = []
    for coefficient in coefficients:
        coefficients_of_variables.append(coefficient.split("+"))

    # get coefficients of constants
    coefficients_of_constants = []
    for constant in constants:
        coefficients_of_constants.append(constant.split("+"))
